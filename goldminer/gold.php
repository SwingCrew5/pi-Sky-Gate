In 1945, unlikely allies toppled Hitler&#039;s war machine, while humanity&#039;s most devastating weapons forced the Japanese Empire to its knees in a storm of fire. Where once there stood many great powers, there then stood only two. The world had scant months to sigh its collective relief before a new conflict threatened. Unlike the titanic struggles of the preceding decades, this conflict would be waged not primarily by soldiers and tanks, but by spies and politicians, scientists and intellectuals, artists and traitors. Twilight Struggle is a two-player game simulating the forty-five year  dance of intrigue, prestige, and occasional flares of warfare between the Soviet Union and the United States. The entire world is the stage on which these two titans fight to make the world safe for their own ideologies and ways of life. The game begins amidst the ruins of Europe as the two new &amp;quot;superpowers&amp;quot; scramble over the wreckage of the Second World War, and ends in 1989, when only the United States remained standing.

Twilight Struggle inherits its fundamental systems from the card-driven classics We the People and Hannibal: Rome vs. Carthage. It is a quick-playing, low-complexity game in that tradition. The game map is a world map of the period, whereon players move units and exert influence in attempts to gain allies and control for their superpower. As with GMT&#039;s other card-driven games, decision-making is a challenge; how to best use one&#039;s cards and units given consistently limited resources?

Twilight Struggle&#039;s Event cards add detail and flavor to the game. They cover a vast array of historical happenings, from the Arab-Israeli conflicts of 1948 and 1967, to Vietnam and the U.S. peace movement, to the Cuban Missile Crisis and other such incidents that brought the world to the brink of nuclear annihilation. Subsystems capture the prestige-laden Space Race as well as nuclear tensions, with the possibility of game-ending nuclear war.

A deluxe edition, published in 2009 includes the following changes from the basic game:

     Mounted map with revised graphics
     Two double-thick counter sheets with 260 counters
     Deck of 110 event cards (increased from 103)
     Revised rules and player aid cards
     Revised at start setup and text change for card #98 Aldrich Ames




Upgrade kit for the owners of the previous version includes the following:

     Mounted Map with revised graphics
     New card decks
     Updated Rules &amp;amp; Charts




